"""
Emit CS2 prediction offsets as a C++ header + JSON.

Output layout:
    namespace Prediction {
    namespace Functions {
        inline constexpr std::ptrdiff_t fnGetUserCmd = 0x...;
        ...
    }
    namespace CCSGOInput {
        inline constexpr std::ptrdiff_t m_commands = 0x...;
        ...
    }
    ...
    } // namespace Prediction
"""

import json
import os
import re
from datetime import datetime, timezone
from typing import Dict, Iterable, List, Tuple

from src.engines.source2.prediction_dumper import CS2PredictionResult


_IDENT_RE = re.compile(r"[^A-Za-z0-9_]")


def _sanitize(name: str) -> str:
    return _IDENT_RE.sub("_", name)


def _group_struct_offsets(
    results: Iterable[CS2PredictionResult],
) -> List[Tuple[str, List[CS2PredictionResult]]]:
    """Group struct_offset results by struct_name, preserving order."""
    buckets: Dict[str, List[CS2PredictionResult]] = {}
    order: List[str] = []
    for r in results:
        key = r.struct_name or "_unknown"
        if key not in buckets:
            buckets[key] = []
            order.append(key)
        buckets[key].append(r)
    return [(k, buckets[k]) for k in order]


# ---------------------------------------------------------------------------
# C++ header writer
# ---------------------------------------------------------------------------

def write_prediction_header(
    path: str,
    functions: List[CS2PredictionResult],
    struct_offsets: List[CS2PredictionResult],
    process_name: str,
) -> None:
    os.makedirs(os.path.dirname(path) or ".", exist_ok=True)
    ts = datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M:%SZ")
    ok_fn = sum(1 for r in functions if r.found)
    ok_so = sum(1 for r in struct_offsets if r.found)

    lines: List[str] = []
    lines.append("// Auto-generated by UE/Unity Dumper - CS2 Prediction Offsets")
    lines.append(f"// Process: {process_name}")
    lines.append(f"// Generated: {ts}")
    lines.append(f"// Functions: {ok_fn}/{len(functions)}  |  Struct offsets: {ok_so}/{len(struct_offsets)}")
    lines.append("#pragma once")
    lines.append("")
    lines.append("#include <cstddef>")
    lines.append("#include <cstdint>")
    lines.append("")
    lines.append("namespace Prediction {")
    lines.append("")

    # Functions block
    lines.append("namespace Functions {")
    for r in functions:
        if r.description:
            lines.append(f"    // {r.description}")
        if r.found:
            lines.append(
                f"    inline constexpr std::ptrdiff_t {_sanitize(r.name)} = 0x{r.rva:X};"
            )
        else:
            lines.append(f"    // [NOT FOUND] {r.name} -- {r.error}")
    lines.append("} // namespace Functions")
    lines.append("")

    # Struct offset blocks — one namespace per struct
    for struct_name, items in _group_struct_offsets(struct_offsets):
        ns = _sanitize(struct_name)
        lines.append(f"namespace {ns} {{")
        for r in items:
            field = r.field_name or r.name
            if r.found:
                lines.append(
                    f"    inline constexpr std::ptrdiff_t {_sanitize(field)} = 0x{r.rva:X};"
                )
            else:
                lines.append(f"    // [NOT FOUND] {field} -- {r.error}")
        lines.append(f"}} // namespace {ns}")
        lines.append("")

    lines.append("} // namespace Prediction")
    lines.append("")

    with open(path, "w", encoding="utf-8") as f:
        f.write("\n".join(lines))


# ---------------------------------------------------------------------------
# JSON writer
# ---------------------------------------------------------------------------

def write_prediction_json(
    path: str,
    functions: List[CS2PredictionResult],
    struct_offsets: List[CS2PredictionResult],
    process_name: str,
) -> None:
    os.makedirs(os.path.dirname(path) or ".", exist_ok=True)
    ts = datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M:%SZ")

    payload: dict = {
        "process": process_name,
        "generated_utc": ts,
        "functions": {},
        "struct_offsets": {},
    }

    for r in functions:
        if r.found:
            payload["functions"][r.name] = {
                "rva": f"0x{r.rva:X}",
                "rva_int": r.rva,
                "description": r.description,
            }
        else:
            payload["functions"][r.name] = {
                "error": r.error,
                "description": r.description,
            }

    for struct_name, items in _group_struct_offsets(struct_offsets):
        struct_map: dict = {}
        for r in items:
            field = r.field_name or r.name
            if r.found:
                struct_map[field] = {
                    "offset": f"0x{r.rva:X}",
                    "offset_int": r.rva,
                    "description": r.description,
                }
            else:
                struct_map[field] = {
                    "error": r.error,
                    "description": r.description,
                }
        payload["struct_offsets"][struct_name] = struct_map

    with open(path, "w", encoding="utf-8") as f:
        json.dump(payload, f, indent=2)
